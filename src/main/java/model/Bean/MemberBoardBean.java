package model.Bean;

public class MemberBoardBean {
	private int mbNum;
	private String mbTitle;
	private String mbContent;
	private String mbDate;
	private String memberId;
	private String mbImg;
	private int moimNum;
	private String boardType;
	public int getMbNum() {
		return mbNum;
	}
	public void setMbNum(int mbNum) {
		this.mbNum = mbNum;
	}
	public String getMbTitle() {
		return mbTitle;
	}
	public void setMbTitle(String mbTitle) {
		this.mbTitle = mbTitle;
	}
	public String getMbContent() {
		return mbContent;
	}
	public void setMbContent(String mbContent) {
		this.mbContent = mbContent;
	}
	public String getMbDate() {
		return mbDate;
	}
	public void setMbDate(String mbDate) {
		this.mbDate = mbDate;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getMbImg() {
		return mbImg;
	}
	public void setMbImg(String mbImg) {
		this.mbImg = mbImg;
	}
	public int getMoimNum() {
		return moimNum;
	}
	public void setMoimNum(int moimNum) {
		this.moimNum = moimNum;
	}
	public String getBoardType() {
		return boardType;
	}
	public void setBoardType(String boardType) {
		this.boardType = boardType;
	}
	
	
}
