package model.Bean;

public class BoardCommentBean {
	private int bcNum;
	private String bcContent;
	private String memberId;
	private String bcDate;
	private int mbNum;
	public int getBcNum() {
		return bcNum;
	}
	public void setBcNum(int bcNum) {
		this.bcNum = bcNum;
	}
	public String getBcContent() {
		return bcContent;
	}
	public void setBcContent(String bcContent) {
		this.bcContent = bcContent;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getBcDate() {
		return bcDate;
	}
	public void setBcDate(String bcDate) {
		this.bcDate = bcDate;
	}
	public int getMbNum() {
		return mbNum;
	}
	public void setMbNum(int mbNum) {
		this.mbNum = mbNum;
	}

	
}
