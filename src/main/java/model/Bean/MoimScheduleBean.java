package model.Bean;

public class MoimScheduleBean {
	private int msNum;
	private String msTime;	
	private String msArea;	
	private int moimNum;
	private int msHCount;
	private String memberId;
	public int getMsNum() {
		return msNum;
	}
	public void setMsNum(int msNum) {
		this.msNum = msNum;
	}
	public String getMsTime() {
		return msTime;
	}
	public void setMsTime(String msTime) {
		this.msTime = msTime;
	}
	public String getMsArea() {
		return msArea;
	}
	public void setMsArea(String msArea) {
		this.msArea = msArea;
	}
	public int getMoimNum() {
		return moimNum;
	}
	public void setMoimNum(int moimNum) {
		this.moimNum = moimNum;
	}
	public int getMsHCount() {
		return msHCount;
	}
	public void setMsHCount(int msHCount) {
		this.msHCount = msHCount;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	
	
}
